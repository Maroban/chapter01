package com.javaex.ex01;
public class Ex09 {
	public static void main(String[] args) {
		//관계 연산자 -- > 결과는 true 또는 false (boolean 형)
		int n01 = 5;
		int n02 = 3;
		System.out.println(n01<n02);	 //false
		System.out.println(n01>n02);	 //true
		
		System.out.println(n01<=n02);	 //false
		System.out.println(n01>=n02);    //true
		
		System.out.println(n01==n02);	 //false
		System.out.println(n01!=n02);	 //true
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
